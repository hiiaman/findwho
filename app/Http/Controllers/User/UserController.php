    function pointInPolygon()
    {
        $point = ["x" => "300", "y" => "8"];

        $minX = 222;
        $maxX = 284;
        "Where x > minX and x < maxX";

        $polygon = [
            ["x"=>"252", "y" => "2"],
            ["x"=>"264", "y" => "4"],
            ["x"=>"272", "y" => "8"],
            ["x"=>"280", "y" => "16"],
            ["x"=>"284", "y" => "23"],
            ["x"=>"256", "y" => "20"],
            ["x"=>"222", "y" => "24"],
            ["x"=>"230", "y" => "12"],
            ["x"=>"243", "y" => "4"]
        ];
        $vertices = $polygon;
        $intersections = 0;
        $vertices_count = count($vertices);

        for ($i=1; $i < $vertices_count; $i++) {
            $vertex1 = $vertices[$i-1];
            $vertex2 = $vertices[$i];

            if ($this->pointOnVertex($point, $vertices) == true) {
                return "vertex";
            }

            if (
                $vertex1['y'] == $vertex2['y'] and
                $vertex1['y'] == $point['y'] and
                $point['x'] > min($vertex1['x'], $vertex2['x']) and
                $point['x'] < max($vertex1['x'], $vertex2['x']))
            {
                return "boundary";
            }
            if (
                $point['y'] > min($vertex1['y'], $vertex2['y']) and
                $point['y'] <= max($vertex1['y'], $vertex2['y']) and
                $point['x'] <= max($vertex1['x'], $vertex2['x']) and
                $vertex1['y'] != $vertex2['y'])
            {
                $xinters = ($point['y'] - $vertex1['y']) * ($vertex2['x'] - $vertex1['x']) / ($vertex2['y'] - $vertex1['y']) + $vertex1['x'];
                if ($xinters == $point['x']) {
                    return "boundary";
                }
                if ($vertex1['x'] == $vertex2['x'] || $point['x'] <= $xinters) {
                    $intersections++;
                }
            }
        }
        if ($intersections % 2 != 0) {
            return "inside";
        } else {
            return "outside";
        }

    }

